"""Overview figure (numeric x-axis) + appendix mapping table.
Run from results/latest/sTiles/ :  python3 plots/gen_overview.py
Outputs (in plots/):
  all_matrices_time.{pdf,png}   per-matrix best time, all libraries
  per_matrix_table.tex          longtable mapping index -> matrix
"""
import csv, os
import matplotlib as mpl; mpl.use('Agg')
import matplotlib.pyplot as plt
mpl.rcParams.update({"font.size":13,"axes.labelsize":14,"legend.fontsize":11,
                     "pdf.fonttype":42,"ps.fonttype":42,"savefig.bbox":"tight"})
HERE=os.path.dirname(os.path.abspath(__file__))
ST=os.path.dirname(HERE); LAT=ST  # results-latest (self-contained)
def base(n): return n[:-4] if n.endswith('.mtx') else n
def load_best(p):
    d={}
    with open(p) as f:
        r=csv.reader(f);h=next(r);bi=h.index('best')
        for x in r:
            if not x:continue
            try:b=float(x[bi])
            except:continue
            if b>0:d[base(x[0])]=b
    return d
def load_canon(p):
    d={}
    for row in csv.DictReader(open(p)):
        try:t=float(row['factor_s'])
        except:continue
        if t<=0:continue
        m=base(row['matrix'])
        if m not in d or t<d[m]:d[m]=t
    return d
# single source of truth: the 88-matrix per-row *_all.csv files (same as the
# table/text/speedup-dist pipeline). The 92-matrix per-core-best subdir CSVs
# (stiles_auto.csv etc.) carried 4 extras (bcsstk12 + 3 lme4) not in the suite.
libs={'sTiles':load_canon(f'{LAT}/stiles_all.csv'),
      'PARDISO':load_canon(f'{LAT}/pardiso_all.csv'),
      'MUMPS':load_canon(f'{LAT}/mumps_all.csv'),
      'PaStiX':load_canon(f'{LAT}/pastix_all.csv'),
      'CHOLMOD':load_canon(f'{LAT}/cholmod_all.csv'),
      'symPACK':load_canon(f'{LAT}/sympack_all.csv')}
# characteristics (class, n, fill)
char={}
for row in csv.DictReader(open(f'{ST}/characteristics.csv')):
    char[row['matrix']]=(row['class'], int(row['n']),
                         row['fill_ratio'])
order=sorted(libs['sTiles'], key=lambda m:libs['sTiles'][m])
x=list(range(1,len(order)+1))

# ---- figure ----
sty={'PARDISO':('#1f77b4','s',4),'MUMPS':('#2ca02c','^',4),
     'PaStiX':('#9467bd','v',4),'CHOLMOD':('#ff7f0e','D',3.5),'symPACK':('#8c564b','x',4)}
fig,ax=plt.subplots(figsize=(13,5.2))
for k,(c,mk,ms) in sty.items():
    ys=[libs[k].get(m) for m in order]
    xs=[i for i,y in zip(x,ys) if y]; yy=[y for y in ys if y]
    ax.scatter(xs,yy,label=k,s=ms*ms*3,c=c,marker=mk,alpha=0.8,linewidths=0.8)
ys=[libs['sTiles'][m] for m in order]
ax.plot(x,ys,'-',color='#d62728',lw=1.6,zorder=6)
ax.scatter(x,ys,label='sTiles',s=22,c='#d62728',marker='o',zorder=7,
           edgecolors='white',linewidths=0.4)
ax.set_yscale('log')
ax.set_xlabel('matrix index (sorted by sTiles factorization time; see Appendix~A)')
ax.set_ylabel('factorization time (s, best over cores)')
ax.grid(True,which='both',axis='y',ls=':',lw=0.5,alpha=0.6)
ax.set_xlim(0,len(order)+1); ax.legend(ncol=3,loc='upper left',framealpha=0.95)
out=f'{HERE}/all_matrices_time'
fig.savefig(out+'.pdf'); fig.savefig(out+'.png',dpi=300); plt.close(fig)

# ---- appendix longtable ----
def esc(s): return s.replace('_','\\_')
rows=[]
for i,m in enumerate(order,1):
    cls,n,fr=char.get(m,('?',0,'?'))
    rows.append(f"{i} & \\texttt{{{esc(m)}}} & {cls} & {n:,} & {fr} \\\\")
tbl=("% auto-generated by plots/gen_overview.py\n"
     "\\begin{longtable}{r l l r r}\n"
     "\\caption{The $88$ benchmark matrices, numbered by their index in "
     "Figure~\\ref{fig:all-matrices} (sorted by sTiles factorization time). "
     "Class is INLA latent-Gaussian-model precision graph or SuiteSparse; "
     "$n$ is the order and fill is $\\mathrm{nnz}(L)/\\mathrm{nnz}(A)$.}"
     "\\label{tab:matrix-index}\\\\\n"
     "\\toprule\n\\# & Matrix & Class & $n$ & fill \\\\\n\\midrule\n\\endfirsthead\n"
     "\\multicolumn{5}{l}{\\small\\itshape Table~\\ref{tab:matrix-index} continued}\\\\\n"
     "\\toprule\n\\# & Matrix & Class & $n$ & fill \\\\\n\\midrule\n\\endhead\n"
     "\\midrule\\multicolumn{5}{r}{\\small\\itshape continued on next page}\\\\\n\\endfoot\n"
     "\\bottomrule\n\\endlastfoot\n"
     + "\n".join(rows) + "\n\\end{longtable}\n")
open(f'{HERE}/per_matrix_table.tex','w').write(tbl)
print("wrote all_matrices_time.{pdf,png} and per_matrix_table.tex for",len(order),"matrices")
